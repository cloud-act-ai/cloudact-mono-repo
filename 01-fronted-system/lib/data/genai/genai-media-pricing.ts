// Helper functions
export function getMediaPricingByProvider(provider: string): GenAIMediaPricing[] {
  return GENAI_MEDIA_PRICING.filter(p => p.provider === provider);
}

export function getMediaPricingByType(mediaType: string): GenAIMediaPricing[] {
  return GENAI_MEDIA_PRICING.filter(p => p.media_type === mediaType);
}

export function getMediaPricingByModel(provider: string, model: string): GenAIMediaPricing[] {
  return GENAI_MEDIA_PRICING.filter(p => p.provider === provider && p.model === model);
}

export function getActiveMediaPricing(): GenAIMediaPricing[] {
  return GENAI_MEDIA_PRICING.filter(p => p.status === 'active');
}

export function getMediaProviders(): string[] {
  return [...new Set(GENAI_MEDIA_PRICING.map(p => p.provider))];
}

export function getMediaTypes(): string[] {
  return [...new Set(GENAI_MEDIA_PRICING.map(p => p.media_type))];
}

export function calculateMediaCost(
  pricing: GenAIMediaPricing,
  units: number,
  useBatch: boolean = false
): number {
  let cost = units * pricing.price_per_unit;
  if (useBatch && pricing.batch_discount_pct > 0) {
    cost = cost * (1 - pricing.batch_discount_pct / 100);
  }
  return cost;
}
